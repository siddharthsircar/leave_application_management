    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        
        try
        {
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            Connection con=DriverManager.getConnection("Jdbc:odbc:abc");
            Statement st=con.createStatement();
            String npwd=txtNpwd.getText();
            String cpwd=txtCnpwd.getText();
            if(npwd.equals("")||cpwd.equals(""))
            {
             JOptionPane.showMessageDialog(rootPane, "Fill All Details.");
            }
            else
            {
            if(cpwd.equals(npwd))
            {
             st.executeUpdate("update details SET pwd='"+cpwd+"' where emp_id='"+id+"'");
             JOptionPane.showMessageDialog(rootPane, "Password Changed!");
             new display(id).setVisible(true);
             this.dispose();
             
            }
            else
            {
             lblError.setText("*Passwords do not match.");
            }
            }
            
        }
        catch(Exception ex)
        {
        System.out.println("Error Because of "+ex);
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnResetActionPerformed

